async def seed():
    async with httpx.AsyncClient(timeout=60) as client:
        for doc in SEED_DOCUMENTS:
            print(f"적재 중: {doc['title']} ...")
            resp = await client.post(f"{BASE_URL}/ingest/text", json=doc)
            if resp.status_code == 200:
                data = resp.json()
                print(f"  → 완료 (document_id={data['document_id']}, chunks={data['chunk_count']})")
            else:
                print(f"  → 실패: {resp.status_code} {resp.text}")

    print("\n시드 데이터 적재 완료!")


if __name__ == "__main__":
    asyncio.run(seed())
